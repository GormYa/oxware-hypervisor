LOOKUP = {"es": EN_ES, "de": EN_DE, "zh": EN_ZH}


def translate_one(en_text: str, target_lang: str) -> str:
    """Translate a single EN string to target lang. Falls back to EN if not in dict."""
    if en_text is None:
        return ""
    raw = en_text.strip()
    if not raw:
        return en_text
    key = raw.lower()
    table = LOOKUP[target_lang]
    if key in table:
        return table[key]
    # try stripping trailing punctuation
    stripped = key.rstrip(".:;!?…—–-")
    if stripped in table:
        return table[stripped]
    # try with English text as-is (acronyms, technical terms)
    if raw in ("VM", "VMs", "ISO", "API", "URL", "CPU", "RAM", "DNS", "DHCP",
              "IP", "MAC", "TCP", "UDP", "TLS", "SSL", "JWT", "OAuth2",
              "SSH", "KVM", "QEMU"):
        return raw
    return en_text  # fallback


def main():
    en_dict = json.load(open(f"{OUT_DIR}/en.json", encoding="utf-8"))
    for lang in ("es", "de", "zh"):
        existing = json.load(open(f"{OUT_DIR}/{lang}.json", encoding="utf-8"))
        full = dict(existing)
        added = 0
        for tr_key, en_val in en_dict.items():
            if tr_key not in full:
                full[tr_key] = translate_one(en_val, lang)
                added += 1
        json.dump(full, open(f"{OUT_DIR}/{lang}_full.json", "w", encoding="utf-8"),
                  ensure_ascii=False, indent=2, sort_keys=True)
        print(f"{lang}: filled {added} new entries, total {len(full)}")


if __name__ == "__main__":
    main()
