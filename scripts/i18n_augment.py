def translate_tr_to_en(tr: str) -> str:
    if tr in TR_EN:
        return TR_EN[tr]
    # Patterns
    if tr.startswith("+ "):
        base = tr[2:]
        if base in TR_EN:
            return "+ " + TR_EN[base]
    if tr.startswith("Tüm "):
        base = tr[4:]
        if base in TR_EN:
            return "All " + TR_EN[base]
    if tr.endswith(" yok") or tr.endswith(" yok."):
        base = tr.rsplit(" yok", 1)[0]
        if base in TR_EN:
            return "No " + TR_EN[base].lower() + ("." if tr.endswith(".") else "")
    if tr.endswith("'lar") or tr.endswith("'ler") or tr.endswith("'lar.") or tr.endswith("'ler."):
        base = tr.rstrip(".").rstrip("'lar").rstrip("'ler")
        if base in TR_EN:
            return TR_EN[base] + "s" + ("." if tr.endswith(".") else "")
    return tr  # fallback: keep TR (still better than nothing)


